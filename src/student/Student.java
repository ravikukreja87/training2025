package student;

public class Student {

	public String name;
	public int age;
	public String grade;

	public void getName() {
		System.out.println("Name is = " + name);
	}

	public void getAge() {
		System.out.println("Age of " + name + " is = " + age);
	}

	public void getGrade() {
		System.out.println("Grade of " + name + " is = " + grade);

	}

}
