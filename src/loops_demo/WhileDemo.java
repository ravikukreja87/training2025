package loops_demo;

public class WhileDemo {
	
	
	public static void main(String[] args) {
		
		
		/*
		
		1. Loop variable, initialization
		2. Condition
		3. Increment/Decrement
		
		
		*/
		
		int i = 1;
		while(i <= 10) {
			System.out.println("Number is = " + i);
			i++;
		}
		
		//Loop body will be executed while(condition TRUE)
		
	}
	

}
